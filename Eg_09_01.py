# INTRO TO DICTIONARIES AND THIER BASIC MANIPULATIONS

purse = dict()                              # Assigns an empty dictionary to a variable, purse. {} is used for dic
purse['money'] = 12                         # Dict uses the key-value pair. ['str']-----key  value is after the =
purse['candy'] = 3                          # assigns 'candy' as the key and 3 as its value (:--separates key from val)
purse['tissues'] = 75
print(purse)
print(purse['candy'])                       # prints value
purse['candy'] = purse['candy'] + 2         # replacing the val of candy by adding 2
print(purse)


# CONSTANTS IN DICT

