# STRING SLICING, CONCATENATION

friends = ['Joseph', 'Glenn', 'Sally']

for friend in friends :
    print('Happy New Year:',  friend)    #SIMILAR CODES

for i in range(len(friends)) :           #USED RANGE AND INDEXING
    friend = friends[i]                  #ITERATES THE VALUE IN THE RANGE AS OBTAINED IN LEN(F)
    print('Happy New Year:',  friend)    #THE OUTCOMES ARE THE SAME