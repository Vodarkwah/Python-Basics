# APPENDING LIST/ ADDING LIST TO AN EMPTY SET

stuff = list()              # AN EMPTY SET
stuff.append('book')        # ADDS BOOK TO THE EMPTY SET
stuff.append(99)            # ADDS 99 TO BOOK
print(stuff)                # PRINT THE CURRENT ITEMS IN 'STUFF'
stuff.append('cookie')      # ADDS COOKIE TO STUFF
print(stuff)


# APPEND CANNOT BE USED IN STRINGS BECAUSE STRINGS ARE IMMUTABLE(CANT CHANGE ANY CHARACTER) UNLIKE LISTS